    class baju extends Menu{
        int id_baju, harga;
        String nama_baju, status;
        
        public baju(){
            this.nama_baju = text_nama_baju.getText();
            this.harga = Integer.parseInt(text_harga_baju.getText());
            this.status = combo_status_baju.getSelectedItem().toString();
        }
    }
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID baju");
        model.addColumn("Nama baju");
        model.addColumn("Harga");
        model.addColumn("Status baju");
        table_baju.setModel(model);
        try {
           this.stat = k.getCon().prepareStatement("select * from baju");
           this.rs = this.stat.executeQuery();
           while (rs.next()) {
               Object[] data={
                   rs.getInt("id_baju"),
                   rs.getString("nama_baju"),
                   rs.getInt("harga"),
                   rs.getString("status")
               };
               model.addRow(data);
           }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_id_baju.setText("");
        text_nama_baju.setText("");
        text_harga_baju.setText("");
    }
