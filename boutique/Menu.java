    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        try {
            baju b = new baju();
            this.stat = k.getCon().prepareStatement("update baju set nama_baju=?,"
            + "harga=?,status=? where id_baju=?");
        stat.setString(1, b.nama_baju);
        stat.setInt(2, b.harga);
        stat.setString(3, b.status);
        stat.setInt(4, Integer.parseInt(text_id_baju.getText()));
        stat.executeUpdate();
        refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inputActionPerformed
        // TODO add your handling code here:
        try {
            baju b = new baju();
            this.stat = k.getCon().prepareStatement("insert into baju values(?,?,?,?)");
            stat.setInt(1, 0);
            stat.setString(2, b.nama_baju);
            stat.setInt(3, b.harga);
            stat.setString(4, b.status);
            stat.executeUpdate();
            refreshTable();
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_inputActionPerformed

